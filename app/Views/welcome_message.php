                e representa o segundo maior investimento em defesa do planeta.<br><br>

                O Exército atua sob o comando da Comissão Militar Central do Partido.<br> 
                Seu Comandante em chefe é o Dirigente do Comitê Militar Central (geralmente o Secretário-Geral do Partido Comunista da China).<br> 
                O Ministro da Defesa Nacional, que opera sob o Conselho de Estado, não exerce nenhuma autoridade sobre o Exército.</p>
