                é o conjunto das forças militares da República Popular da China e do Partido Comunista da China.<br> 
                O Exército é composto por cinco braços principais, sendo estes: Forças Terrestres,<br> 
                Força Aérea, Marinha, e ainda a Força de Mísseis Balísticos e a Força de Apoio estratégico.<br> 
